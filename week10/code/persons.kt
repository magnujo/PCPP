/**
 * The purpose of this object is to generate random Person objects
 * The person() method takes as argument the number of persons to be generated
 * All persons generated will have unique phone numbers within a single call
 * to persons(), but not between two calls to persons().
 * @author kasper
 */
object PersonGenerator {


    fun persons(N: Int) = sequence(){
        for (phone in phoneNumbers(N)) {
            yield ( Person(name(), phone, AddressGenerator.address(200)) )
        }
    }

    private fun phoneNumbers(N: Int): Iterable<String> {
        if (N >10000000) throw IllegalArgumentException("Can at most make 10.000.000 phonenumbers")
        val numbers = HashSet<String>()
        while (numbers.size < N) {
            numbers.add(phoneNumber())
        }
        return numbers
    }

    private fun phoneNumber(): String {
        val rnd = Random()
        val first = rnd.nextInt(50) + 20 // from 20 to 69
        val second = rnd.nextInt(100)
        val third = rnd.nextInt(100)
        val forth = rnd.nextInt(100)
        return String.format("%02d%02d-%02d%02d", first, second, third, forth)
    }


    private fun name(): String {
        val rnd = Random()
        val first = FIRSTNAMES[rnd.nextInt(FIRSTNAMES.size)]
        val last = LASTNAMES[rnd.nextInt(LASTNAMES.size)]
        return "$first $last"
    }


}
