fun Person.nice(): String {
    return "${this.name} (${phoneNo}) ${this.address.address}; ${this.address.zip} ${this.address.city}"
}


class PersonTree() {
  private companion object { // Kotlin speak for static members
      val Noone = Person("","",Address("","",""))
      val Empty = Node(Noone)
  }

  private var root : Node = Empty

  private class Node (val person: Person = Noone) {
      var left : Node = Empty
      var right: Node = Empty

      fun insert(pers: Person):Node {
        // return a Node/Tree with pers inserted
          if (this == Empty) return Node(pers)
          if (pers.name < person.name){
              left = left.insert(pers)
          } else {
              right = right.insert(pers)
          }
          return this
      }
      
      fun sorted() : Sequence<Person> = sequence {
        /* return sorted( left) + person + sorted( right)*/
          if (this@Node != Empty) { // if no @Node, this is the sequence
              yieldAll( left.sorted() )
              yield(person)
              yieldAll( right.sorted() )
          }
      }
  }


  public fun insert(pers : Person){
      root = root.insert(pers)
  }

  fun sorted() = root.sorted()

}

